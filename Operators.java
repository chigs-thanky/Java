public class Operators {
    public static void main(String[] args) {
        int sum1 = 10 + 5;
        System.out.println(sum1);
        int sum2 = sum1 + 5;
        System.out.println(sum2);
        int sum3 = sum1 + sum2;
        System.out.println(sum3);
        
        int x = 50, y = 10;
        System.out.println("x = " + x + "\ny = " + y);
        System.out.println("x/y: " + x / y);
        x++;
        System.out.println(x);

    }

}