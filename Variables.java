public class Variables {
    public static void main(String[] args){
        //String DataType
        String name = "Chirag";
        System.out.println(name);

        //Int DataType
        int myDate = 12;
        System.out.println(myDate);

        //Float DataType
        float weight = 65.67f;
        System.out.println(weight);

        //Char DataType
        char initial = 'C';
        System.out.println(initial);
    
    }
}
